import java.util.*;
class SumSpecial{
    public static void main(String args[]){
        int a,b,c;
        Scanner sc=new Scanner(System.in);
        System.out.println("Enter A and B Values: ");
        a=sc.nextInt();
        b=sc.nextInt();
        c=a+b;
        System.out.println("Sum :"+c);
    }
}